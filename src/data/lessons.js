/* 하위 호환용 플랫 배열 — curriculum에서 자동 생성 */
export const allLessons = curriculum.flatMap(semester =>
    semester.chapters.flatMap(chapter =>
        chapter.lessons.map(lesson => {
            const impl = implementedLessons[lesson.id]
            return {
                id: lesson.id,
                title: lesson.title,
                chapter: chapter.title,
                grade: semester.grade,
                icon: semester.icon,
                description: lesson.title,
                totalSteps: impl ? impl.totalSteps : 0,
                color: semester.color,
                locked: !impl,
            }
        })
    )
)
